#include "libft.h"

char	*ft_strnstr(const char *big, const char *little, size_t len)
{
	size_t	l_len;

	l_len = ft_strlen(little);
	if (!l_len)
		return ((char *)big);
	while (*big && l_len <= len--)
	{
		if (!ft_memcmp(big, little, l_len))
			return ((char *)big);
		big++;
	}
	return (0);
}
